const instance = axios.create({
  baseURL: import.meta.env.VITE_BE_API_URL || "/api", // Use proxy path
  headers: {
    "Content-Type": "application/json",
  },
  withCredentials: false, // T·∫Øt credentials ƒë·ªÉ tr√°nh CORS issue
});


instance.interceptors.request.use(
  (config) => {
    // NProgress.start(); // Commented out temporarily
    
    // Debug log for API calls
    console.log(`üöÄ API Request: ${config.method?.toUpperCase()} ${config.url}`, config);
    
    // T·ª± ƒë·ªông th√™m token v√†o header n·∫øu c√≥
    const token = localStorage.getItem('accessToken'); // S·ª≠a t·ª´ 'token' th√†nh 'accessToken'
    if (token) {
      config.headers.Authorization = `Bearer ${token}`;
    }
    
    return config;
  },
  (error) => {
    // NProgress.done(); // Commented out temporarily
    return Promise.reject(error);
  }
);

// Interceptor response: t·∫Øt NProgress v√† x·ª≠ l√Ω l·ªói authentication
instance.interceptors.response.use(
  (response) => {
    // NProgress.done(); // Commented out temporarily
    console.log(`‚úÖ API Success: ${response.config.method?.toUpperCase()} ${response.config.url}`, response);
    return response.data;
  },
  (error) => {
    // NProgress.done(); // Commented out temporarily
    
    // Debug log for API errors
    console.error(`‚ùå API Error: ${error.config?.method?.toUpperCase()} ${error.config?.url}`, {
      status: error.response?.status,
      statusText: error.response?.statusText,
      data: error.response?.data,
      error: error.message
    });
    
    // X·ª≠ l√Ω l·ªói 405 (Method Not Allowed)
    if (error.response?.status === 405) {
      console.error('üö´ 405 Method Not Allowed - The endpoint may not exist or the HTTP method is not supported');
      console.error('Check if the API endpoint exists and supports the HTTP method being used');
    }
    
    // X·ª≠ l√Ω l·ªói 401 (Unauthorized) - Token h·∫øt h·∫°n ho·∫∑c kh√¥ng h·ª£p l·ªá
    if (error.response?.status === 401) {
      // Ch·ªâ redirect v·ªÅ login n·∫øu kh√¥ng ph·∫£i l√† request update profile
      const isUpdateProfile = error.config?.url?.includes('/user-accounts/me/info');
      
      if (!isUpdateProfile) {
        // Clear localStorage v√† redirect v·ªÅ login
        localStorage.removeItem('currentUser');
        localStorage.removeItem('accessToken'); // S·ª≠a t·ª´ 'token' th√†nh 'accessToken'
        
        // Ch·ªâ redirect n·∫øu kh√¥ng ph·∫£i ·ªü trang login
        if (window.location.pathname !== '/login') {
          window.location.href = '/login';
        }
      }
    }
    
    return Promise.reject(error);
  }
);

export default instance;